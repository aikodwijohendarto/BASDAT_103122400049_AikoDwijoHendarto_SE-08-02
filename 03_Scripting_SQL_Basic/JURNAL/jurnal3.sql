CREATE TABLE JADWAL (
ID_JADWAL CHAR(5) PRIMARY KEY,
ID_FILM CHAR(5),
ID_THEATER CHAR(5),
JAM_TAYANG VARCHAR2(10),

CONSTRAINT FK_JADWAL_FILM
FOREIGN KEY (ID_FILM)
REFERENCES FILM(ID_FILM),

CONSTRAINT FK_JADWAL_THEATER
FOREIGN KEY (ID_THEATER)
REFERENCES THEATER(ID_THEATER)
);

