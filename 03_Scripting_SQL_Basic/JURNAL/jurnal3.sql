SELECT
ID_THEATER,
KELAS,
HARGA AS HARGA_AWAL,
HARGA*0.85 AS HARGA_SETELAH_DISKON
FROM THEATER;

SELECT
j.ID_THEATER,
SUM(t.HARGA * tr.JUMLAH_TIKET) AS TOTAL_PEMASUKAN
FROM TRANSAKSI tr
JOIN JADWAL j ON tr.ID_JADWAL = j.ID_JADWAL
JOIN THEATER t ON j.ID_THEATER = t.ID_THEATER
GROUP BY j.ID_THEATER;